// class in oop 
class Rect {
    constructor(width,height){
        this.w1 = width
        this.h1 = height
    }
    calArea(){
        return this.w1 * this.h1
    }
    calRang(){
        return 2 * (this.w1 * this.h1)
    }
}
var rect1 = new Rect(10,20)
var rect2 = new Rect(50,20)
var rect3 = new Rect(10,20)

