// js nested loops 

for(var i = 1; i<= 10; i++){
    var str = '';
    for(var j = 1; j <= i; j++){
        str += j + ' '
    }

    console.log(str)
}

var n = 10
for(var i = 1; i<= n; i++){
    var str = '';
    for(var j = 1; j <= n; j++){
        str += '* '
        // console.log(j)
    }

    console.log(str)
}








